package jdbc_maven;

public class User {
	private int id;
	private String userName;
	private String password;
	private String email;
	private long phone;
	private String fbPassword;
	private String instaPassword;
	private String twitterPassword;
	private String whatsAppPassword;
	private String telegramPassword;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public long getPhone() {
		return phone;
	}
	public void setPhone(long phone) {
		this.phone = phone;
	}
	public String getFbPassword() {
		return fbPassword;
	}
	public void setFbPassword(String fbPassword) {
		this.fbPassword = fbPassword;
	}
	public String getInstaPassword() {
		return instaPassword;
	}
	public void setInstaPassword(String instaPassword) {
		this.instaPassword = instaPassword;
	}
	public String getTwitterPassword() {
		return twitterPassword;
	}
	public void setTwitterPassword(String twitterPassword) {
		this.twitterPassword = twitterPassword;
	}
	public String getWhatsAppPassword() {
		return whatsAppPassword;
	}
	public void setWhatsAppPassword(String whatsAppPassword) {
		this.whatsAppPassword = whatsAppPassword;
	}
	public String getTelegramPassword() {
		return telegramPassword;
	}
	public void setTelegramPassword(String telegramPassword) {
		this.telegramPassword = telegramPassword;
	}
	

}
